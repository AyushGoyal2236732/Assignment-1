#include<iostream>
using namespace std;
int main(){
     //Product of Two Numbers
    int a,b;
    cin>>a>>b;
    cout<<a*b<<endl;
    

    //Type-Casting
     a = 'U';
    cout<<a<<endl;

    //Rectangle
     a=2,b=3;
    cout<<a*b<<endl;
    
    //Product of Three Numbers or Cubes
     a=2;
    cout<<a*a*a<<endl;

    //Size of an Integer
     a=2;
    cout<<sizeof(a)<<endl;
    
    //Swapping of Two Numbers
     a=2,b=3;
    cout<<a<<" "<<b<<endl; 
    int temp;
    temp = a;
    a = b;
    b = temp;

    cout<< a<<" "<<b<<endl;

    return 0;


}